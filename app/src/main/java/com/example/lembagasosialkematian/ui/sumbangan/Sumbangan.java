package com.example.lembagasosialkematian.ui.sumbangan;

public class Sumbangan {
    private String Penyumbang, Tanggal, Jumlah, Dusun, Id;

    public String getId() {
        return Id;
    }

    public void setId(String id) {
        Id = id;
    }

    public String getPenyumbang() {
        return Penyumbang;
    }

    public void setPenyumbang(String penyumbang) {
        Penyumbang = penyumbang;
    }

    public String getTanggal() {
        return Tanggal;
    }

    public void setTanggal(String tanggal) {
        Tanggal = tanggal;
    }

    public String getJumlah() {
        return Jumlah;
    }

    public void setJumlah(String jumlah) {
        Jumlah = jumlah;
    }

    public String getDusun() {
        return Dusun;
    }

    public void setDusun(String dusun) {
        Dusun = dusun;
    }
}
